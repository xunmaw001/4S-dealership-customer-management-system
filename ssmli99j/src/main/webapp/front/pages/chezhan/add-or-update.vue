																																																																																																																																																																																																																																																																																																																																																																																																																																																																																																																																																																																																																																																																																																																																if((!this.ruleForm.qichemingcheng)){
					this.$utils.msg(`汽车名称不能为空`);
					return
				}
																																								if((!this.ruleForm.qichefenlei)){
					this.$utils.msg(`汽车分类不能为空`);
					return
				}
																																								if((!this.ruleForm.qichepinpai)){
					this.$utils.msg(`汽车品牌不能为空`);
					return
				}
																																								if((!this.ruleForm.qicheyanse)){
					this.$utils.msg(`汽车颜色不能为空`);
					return
				}
																																																																																																																																																																																																																																												if(this.ruleForm.shuliang&&(!this.$validate.isIntNumer(this.ruleForm.shuliang))){
					this.$utils.msg(`数量应输入整数`);
					return
				}
																																																																																																								if(this.ruleForm.clicknum&&(!this.$validate.isIntNumer(this.ruleForm.clicknum))){
					this.$utils.msg(`点击次数应输入整数`);
					return
				}
																																				if((!this.ruleForm.userid)){
					this.$utils.msg(`用户id不能为空`);
					return
				}
																																				if(this.ruleForm.id){
					await this.$api.update(`chezhan`, this.ruleForm);
				}else{
					await this.$api.add(`chezhan`, this.ruleForm);
				}
				this.$utils.msgBack('提交成功');
			},
